void reportValue(uint8_t row, float value, char *label, char *unit)
{
    VT100.setCursor(row, 4);
    VT100.setTextColor(VT_YELLOW);
    Serial.print(label);

    VT100.setCursor(row, 19);
    VT100.setTextColor(VT_WHITE);
    Serial.print(value);

    VT100.setCursor(row, 46);
    Serial.println(unit);
}

